# Q1:
# In Python, what is the difference between a list, a tuple, and a set?
# Please include:

# Mutability

# Ordering

# Whether duplicates are allowed

# Typical use cases





# Q1 Answer:
# Lists and sets are mutable, tuples are immutable. Lists and tuples are ordered, sets are not ordered. Lists and tuples allow duplicates, sets automatically remove duplicates

# Lists → general-purpose sequences

# Tuples → fixed collections, dictionary keys

# Sets → membership tests, removing duplicates